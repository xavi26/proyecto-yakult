    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

      jLabel17.setText(jTextField1.getText());
      jLabel18.setText(jTextField2.getText());
      jLabel19.setText(jTextField3.getText());
      jLabel20.setText(jTextField4.getText());
      jLabel21.setText(jTextField5.getText());
      jLabel22.setText(jTextField6.getText());
      jLabel24.setText(jTextArea1.getText());
      if(jRadioButton1.isSelected()&&jRadioButton2.isSelected()){
          jLabel25.setText("Visita rlizada y producto entregado");
      }
      else if(jRadioButton1.isSelected())
      {
          jLabel25.setText("Visita realizada ");
      }
      else if (jRadioButton2.isSelected())
      {
          jLabel25.setText("Producto entregada ");
      }
      else
      {
          jLabel25.setText("El cliente no ha sido visitado ni el producto entregado");
      }
      jTextField1.setText(" ");
      jTextField2.setText(" ");
      jTextField3.setText(" ");
      jTextField4.setText(" ");
      jTextField5.setText(" ");
      jTextField6.setText(" ");
      jTextArea1.setText(" ");
      if(jRadioButton1.isSelected())
      {
          jRadioButton1.doClick();
      }
      if(jRadioButton2.isSelected())
      {
          jRadioButton2.doClick();
      }
      
      jPanel1.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

